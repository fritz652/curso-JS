// Operadores de desigualdad estricta

console.log(9 != 6); // el operador != es de desigualdad

console.log(9 != "9"); // false, porque son iguales para el operador de desigualdad
console.log(9 !== "9"); // true, porque son diferente para el operador de desigualdad estricta
console.log([1,2,3] !== [1,2,3]); //true, el operador de desigualdad no puede comprar objetos, por eso arroja true.