
// Sentencias Switch ejemplo -02

var producto = "hamburguesa";

switch (producto) {
    case "Pizza":
        console.log("La pizza basica cuesta $10.55");
        break;
    case "hamburguesa":
        console.log("Las hamburguesas cuestas $6.78");
        break;
    case "helado":
        console.log("El helado cuesta $2.8");
        break;
}
